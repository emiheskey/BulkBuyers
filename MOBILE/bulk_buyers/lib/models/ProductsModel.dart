class ProductsModel{
String name;
String title;
int price;
String imageUrl;
String description;
String category;

ProductsModel(String name, String title, int price, String imageUrl, String description, String category){
  this.name = name;
  this.title = title;
  this.price = price;
  this.imageUrl = imageUrl;
  this.description = description;
  this.category = category;
}

}